p = [0]+list(map(int, input()))
dp = [0]*(len(p))
if p[1] == 0:
    print(0)
else:
    dp[0] = dp[1] = 1
    for i in range(2, len(p)):
        if p[i] > 0:
            dp[i] += dp[i-1]
        if 10 <= p[i-1]*10+p[i] <= 26:
            dp[i] += dp[i-2]
    print(dp[-1] % 1000000)

